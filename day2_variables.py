#This is a declaration (implicitly done with assignment)
name = "Zak Moorcroft" 

#This is an Assignment (the value 28 is assigned to the variable 'age')
age = 28

#This is an initialization (the variable is initialized with the value 'blue')
favorite_colour = "blue"

print(f"My name is {name}.")
print(f"My age is {age}.")
print(f"my favouite colour is {favorite_colour}.")

#Summary
#declaration - introducing a variable
#Assignment - Giving a value to a variable (can happen multiple times)
#Initialization - the first assignment of a value to a variable, usually done at declaration.
