# This is a program to read different data type inputs and print them

# Reading an integer
int_input = int(input("Please enter an integer:" ))
print(f"You entered the integer: {int_input}")

# Reading a floating-point number
float_input = float(input("Please enter a floating-point number: "))
print(f"You entered the floating-point number: {float_input}")

# Reading a string
string_input = input("Please enter a string ")
print(f"You entered the string: '{string_input}'")
