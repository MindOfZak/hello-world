#Program to calculate the area of a rectangle (integer input)

# Prompt the user for length and width
length = int(input("Please enter the length of the rectangle (integer): "))
width = (int(input("Please enter the width of the rectangle (integer): ")))

# Calculate the area
area = length * width

# Print the result
print(f"The area of the rectangle with length {length} and width {width} is {area}.")
